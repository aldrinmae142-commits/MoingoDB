// Conditional statements
let num = 10;
if (num > 0) {
    console.log("Positive");
} else if (num < 0) {
    console.log("Negative");
} else {
    console.log("Zero");
}

// Loops
for (let i = 0; i < 5; i++) {
    console.log("Loop " + i);
}

// Functions
function greet(name) {
    return "Hello " + name;
}
console.log(greet("World"));